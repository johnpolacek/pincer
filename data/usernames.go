import (
	"math/rand"
	"strings"
	"time"
)

func RandomUsername() string {
	rand.Seed(time.Now().UnixNano())
	u := UserNames[rand.Intn(len(UserNames))]

	if rand.Intn(5) == 0 {
		u = strings.ToLower(u)
	}

	if rand.Intn(5) == 0 {
		u = strings.ToUpper(u)
	}

	switch rand.Intn(5) {
	case 0:
		u = strings.Replace(u, " ", "-", -1)
	case 1:
		u = strings.Replace(u, " ", "_", -1)
	case 2:
		u = strings.Replace(u, " ", "", -1)
	default:
		u = strings.Replace(u, " ", "", -1)
	}

	return u
}

func RandomUsernames(count int) []string {
	usernames := []string{}
	for i := 0; i < count; i++ {
		usernames = append(usernames, RandomUsername())
	}
	return usernames
}

